public class Horse
{
    private String name;
    private String colour;
    private int DOB;

    public String getName() {
        return name;
    }

    public String getColour() {
        return colour;
    }

    public int getDOB() {
        return DOB;
    }

    public void setName(String n) {
        name = n;
    }

    public void setColour(String c) {
        colour = c;
    }

    public void setDOB(int y) {
        DOB = y;
    }
}